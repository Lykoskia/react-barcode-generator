/* MODFIX WORKAROUND AS AN ALTERNATIVE TO BIGINT */

const modFix = (dividend, divisor) => {
    return [...dividend].reduce((x, digit) => (x * 10 + parseInt(digit)) % divisor, 0);
};

/* GENERATE CROATIAN IBAN FROM BANK CODE AND ACCOUNT NUMBER */

export const generateIBAN = (bankCode, accountNumber) => {
    const countryCode = "HR";
    const initialIBAN = bankCode + accountNumber + '172700'; // HR00
    const integer_fixed_IBAN = initialIBAN.replace(/[A-Z]/g, c => c.charCodeAt(0) - 55);
    const mod = modFix(integer_fixed_IBAN, 97);
    const checkDigits = (98 - mod).toString().padStart(2, '0');
    return countryCode + checkDigits + bankCode + accountNumber;
};

